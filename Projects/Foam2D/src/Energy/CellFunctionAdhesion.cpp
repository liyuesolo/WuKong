void
CellFunctionAdhesion::addValue(const VectorXT &site, const VectorXT &nodes, const VectorXi &next, const VectorXi &btype,
                               double &value,
                               const CellInfo *cellInfo) const {
    int n_nodes = nodes.rows() / nx;

    double x0, y0, q0, x1, y1, q1;
    int x0i, y0i, q0i, x1i, y1i, q1i;
    for (int i = 0; i < n_nodes; i++) {
        double a = cellInfo->neighbor_affinity(i);

        x0i = i * nx + 0;
        y0i = i * nx + 1;
        q0i = i * nx + 2;
        x1i = next(i) * nx + 0;
        y1i = next(i) * nx + 1;
        q1i = next(i) * nx + 2;

        x0 = nodes(x0i);
        y0 = nodes(y0i);
        q0 = nodes(q0i);
        x1 = nodes(x1i);
        y1 = nodes(y1i);
        q1 = nodes(q1i);

        // @formatter:off
        if (btype(i) == 0 || (btype(i) == 2 && fabs(sin(q1 - q0)) < 1e-3)) {
            value += a * ((x1 - x0) * (x1 - x0) + (y1 - y0) * (y1 - y0));
        } else if (btype(i) == 1) {
            value += 0.4e1 * a * q0 * q0 * pow(asin(sqrt(pow(x1 - x0, 0.2e1) + pow(y1 - y0, 0.2e1)) / q0 / 0.2e1), 0.2e1);
        } else if (btype(i) == 2) {
            double t2 = cos(q1);
            double t4 = sin(q1);
            double t7 = cos(q0);
            double t9 = sin(q0);
            double t10 = t2 * t9;
            double t13 = t7 * t4;
            double t15 = 0.1e1 / (t13 - t10);
            double t16 = t15 * (t7 * (t2 * (y0 - y1) + x1 * t4) - x0 * t10);
            double t17 = -x0 + t16;
            double t21 = 0.2e1 * x0 - 0.4e1 * t16 + 0.2e1 * x1;
            double t22 = 0.2e1 * t21 * t17;
            double t30 = t15 * (t9 * (t4 * (-x0 + x1) - y1 * t2) + y0 * t13);
            double t31 = -y0 + t30;
            double t35 = 0.2e1 * y0 - 0.4e1 * t30 + 0.2e1 * y1;
            double t36 = 0.2e1 * t35 * t31;
            double t37 = t21 * t21;
            double t38 = t35 * t35;
            double t39 = t22 + t36 + t37 + t38;
            double t42 = 0.4e1 * t17 * t17;
            double t43 = 0.4e1 * t31 * t31;
            double t45 = sqrt(t37 + t38 + 0.2e1 * t22 + 0.2e1 * t36 + t42 + t43);
            double t47 = t22 + t36;
            double t48 = t42 + t43;
            double t49 = sqrt(t48);
            double t52 = t37 + t38;
            double t56 = sqrt(t52);
            double t57 = 0.1e1 / t56;
            double t61 = log(t57 * t47 + t49);
            double t64 = log(t57 * t39 + t45);
            double t76 = pow(0.1e1 / t52 * (0.2e1 * t45 * t39 - 0.2e1 * t49 * t47) / 0.4e1 +
                      0.1e1 / t56 / t52 * (0.4e1 * t47 * t47 - 0.4e1 * t52 * t48) * (t61 - t64) / 0.8e1, 0.2e1);
            value += t76 * a;
        }
        // @formatter:on
    }
}

